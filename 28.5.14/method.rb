def say_goodnight(name)
 result = "good night, "+name
 return result
end
puts say_goodnight("jayasenthil")
puts say_goodnight("kumar")

def say_bye(name)
 result = "Bye #{name}"
 return result
end
puts say_bye("Friends")

def say_bye_capital(name)
 result = "Bye #{name.capitalize}"
 return result
end
puts say_bye_capital("anand")

